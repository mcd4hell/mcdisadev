// Current language ‚Äî default from localStorage or browser
let currentLang = localStorage.getItem('mcd-lang') || (navigator.language.startsWith('tr') ? 'tr' : 'en');

// Apply translations to all [data-i18n] elements
function applyTranslations() {
    const t = translations[currentLang];
    if (!t) return;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) {
            el.textContent = t[key];
        }
    });

    // Placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (t[key] !== undefined) {
            el.setAttribute('placeholder', t[key]);
        }
    });

    // Update lang button label
    const langBtnLabel = document.getElementById('langBtnLabel');
    if (langBtnLabel) {
        langBtnLabel.textContent = currentLang.toUpperCase();
    }

    // Highlight active option in dropdown
    document.querySelectorAll('.lang-option').forEach(opt => {
        opt.classList.toggle('active', opt.dataset.lang === currentLang);
    });
}

// Set language
function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';
    currentLang = lang;
    localStorage.setItem('mcd-lang', lang);
    document.documentElement.lang = lang;
    applyTranslations();
}

// Initialize language picker + i18n
function initI18n() {
    // Build the floating language picker into the DOM
    const picker = document.createElement('div');
    picker.className = 'lang-picker';
    picker.id = 'langPicker';
    picker.innerHTML = `
        <button class="lang-picker-btn" id="langPickerBtn" title="Change language">
            <i class="fas fa-globe"></i>
            <span id="langBtnLabel">${currentLang.toUpperCase()}</span>
            <i class="fas fa-chevron-down lang-picker-arrow"></i>
        </button>
        <div class="lang-dropdown" id="langDropdown">
            <button class="lang-option ${currentLang === 'en' ? 'active' : ''}" data-lang="en">
                <span class="lang-flag">üá¨üáß</span>
                <span>English</span>
            </button>
            <button class="lang-option ${currentLang === 'tr' ? 'active' : ''}" data-lang="tr">
                <span class="lang-flag">üáπüá∑</span>
                <span>T√ºrk√ße</span>
            </button>
        </div>
    `;
    document.body.appendChild(picker);

    // Toggle dropdown
    const btn = document.getElementById('langPickerBtn');
    const dropdown = document.getElementById('langDropdown');

    btn.addEventListener('click', (e) => {
        e.stopPropagation();
        picker.classList.toggle('open');
    });

    // Language option click
    dropdown.querySelectorAll('.lang-option').forEach(opt => {
        opt.addEventListener('click', () => {
            setLanguage(opt.dataset.lang);
            picker.classList.remove('open');
        });
    });

    // Close dropdown on outside click
    document.addEventListener('click', () => {
        picker.classList.remove('open');
    });

    // Set initial lang
    document.documentElement.lang = currentLang;
    applyTranslations();
}

// Auto-init on DOM ready
document.addEventListener('DOMContentLoaded', initI18n);

// Export
window.i18n = {
    setLanguage,
    getCurrentLang: () => currentLang,
    t: (key) => translations[currentLang]?.[key] || key
};